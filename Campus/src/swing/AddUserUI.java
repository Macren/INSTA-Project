    private void bt_addUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_addUserActionPerformed
        // TODO add your handling code here:
        
         // getting data from form
        // ----------------------
        String          aLogin = this.tf_login.getText();
        String          cleanPwd = new String(this.tf_passwd.getPassword());
        String          cryptPwd = PasswordUtils.getMD5Str(cleanPwd); //crypt password using MD5
        String          strType = this.combob_type.getSelectedItem().toString();
        String          aFirstName = this.tf_firstName.getText();
        String          aLastName = this.tf_lastName.getText();
        String          aMail = this.tf_mail.getText();
        Calendar        myCal = Calendar.getInstance(); // set a calendar to init sql.Date
        myCal.set(Calendar.YEAR, ((Integer)combob_year.getSelectedItem()).intValue());
        myCal.set(Calendar.MONTH, ((Integer)combob_month.getSelectedItem()).intValue());
        myCal.set(Calendar.DAY_OF_MONTH, ((Integer)combob_day.getSelectedItem()).intValue());
        Date            aBirthDate = new Date(myCal.getTime().getTime());
        Education       anEdu = (Education)this.combob_education.getSelectedItem();
        AbstractUser    aUser = null;
        UserType        anUserType = null;
        
        System.out.println("type d'utilisateur: " + strType);
        System.out.println("login: " + aLogin);
        System.out.println("clean pass: " + cleanPwd);
        System.out.println("crypt pass: " + cryptPwd);
        System.out.println("mail: " + aMail);
        System.out.println("first name: " + aFirstName);
        System.out.println("last name: " + aLastName);
        System.out.println("date de naissance:" + aBirthDate);
        System.out.println("formation:" + anEdu);
        System.out.println("√©cole:" + this.mySchool);

        // setting user type
        // -----------------
        if (strType.compareTo("Eleve") == 0) { // a student
            anUserType = UserType.STUDENT;
            aUser = new Student(aLogin, cryptPwd, aMail, aBirthDate, aFirstName, aLastName, 0, this.mySchool, anEdu);
            System.out.println(aUser);
            StudentDAO studentDAO = new StudentDAO();
            studentDAO.insert((Student)aUser);
        }
        else if (strType.compareTo("Professeur") == 0) { // a teacher
            anUserType = UserType.TEACHER;
            aUser = new Teacher(aLogin, cryptPwd, aMail, aBirthDate, aFirstName, aLastName, 0, this.mySchool, null);
            System.out.println(aUser);
            TeacherDAO teacherDAO = new TeacherDAO();
            teacherDAO.insert((Teacher)aUser);
        }
        else if (strType.compareTo("Administration") == 0) { // an admin user
            anUserType = UserType.ADMIN;
            aUser = new Administrator(aLogin, cryptPwd, aMail, aBirthDate, aFirstName, aLastName, 0, this.mySchool, null);
            System.out.println(aUser);
            AdministratorDAO adminDAO = new AdministratorDAO();
            adminDAO.insert((Administrator)aUser);
        }
        
        this.dispose();
    }//GEN-LAST:event_bt_addUserActionPerformed

